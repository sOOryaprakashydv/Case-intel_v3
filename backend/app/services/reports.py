"""
Report generation (Section 7.16). Produces PDF, HTML, and CSV, and logs
every generation to the Report table so history is a first-class list
rather than an afterthought.
"""
import csv
import io
import os
from datetime import datetime

from jinja2 import Template
from reportlab.lib.pagesizes import letter
from reportlab.lib import colors
from reportlab.lib.units import inch
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle

from ..config import settings
from ..models import Case, Report

# NOTE: PDF generation uses reportlab (pure Python, no native system
# libraries required) rather than WeasyPrint. WeasyPrint depends on
# Pango/Cairo/GDK-Pixbuf at the OS level, which Render's default Python
# buildpack does not install — that would break PDF export at build or
# runtime on a free-tier deploy. reportlab ships as a normal pip wheel.

